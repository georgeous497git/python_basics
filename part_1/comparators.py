print(f'{num1} less than {num2}: ' + str(num1 < num2))
print(f'{num1} equals: {num2}: ' + str(num1 == num2))
print(f'{num1} greater than or equals {num2}: ' + str(num1 >= num2))
print(f'{num1} less than or equals {num2}: ' + str(num1 <= num2))
print(f'{num1} different of {num2}: ' + str(num1 != num2))

